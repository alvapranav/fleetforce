from sqlalchemy import Column, Integer, String, Float, JSON
from sqlalchemy.orm import declarative_base

Base = declarative_base()

class Stops(Base):
    __tablename__ = 'stops'
    tractor_id = Column(String, primary_key=True, index=True)
    trip_id = Column(String, primary_key=True, index=True)
    stop_type = Column(String, primary_key=True, index=True)
    latitude = Column(Float)
    longitude = Column(Float)
    arrival_datetime = Column(String, primary_key=True, index=True)
    departure_datetime = Column(String)
    miles_travelled = Column(Float)
    dwell_time = Column(Float)
    fuel_latitude = Column(Float)
    fuel_longitude = Column(Float)
    fuel_tank_percent_before = Column(Float)
    fuel_tank_percent_after = Column(Float)
    fuel_location_name = Column(String)
    unit_price = Column(Float)
    total_cost = Column(Float)
    quantity = Column(Float)
    city = Column(String)
    state = Column(String)
    is_warehouse = Column(String)
    nearest1_name = Column(String)
    nearest1_amenity = Column(String)
    nearest1_building = Column(String)
    nearest1_shop = Column(String)
    nearest1_highway = Column(String)
    nearest1_latitude = Column(Float)
    nearest1_longitude = Column(Float)
    nearest2_name = Column(String)
    nearest2_amenity = Column(String)
    nearest2_building = Column(String)
    nearest2_shop = Column(String)
    nearest2_highway = Column(String)
    nearest2_latitude = Column(Float)
    nearest2_longitude = Column(Float)
    nearest3_name = Column(String)
    nearest3_amenity = Column(String)
    nearest3_building = Column(String)
    nearest3_shop = Column(String)
    nearest3_highway = Column(String)
    nearest3_latitude = Column(Float)
    nearest3_longitude = Column(Float)
    nearest4_name = Column(String)
    nearest4_amenity = Column(String)
    nearest4_building = Column(String)
    nearest4_shop = Column(String)
    nearest4_highway = Column(String)
    nearest4_latitude = Column(Float)
    nearest4_longitude = Column(Float)
    nearest5_name = Column(String)
    nearest5_amenity = Column(String)
    nearest5_building = Column(String)
    nearest5_shop = Column(String)
    nearest5_highway = Column(String)
    nearest5_latitude = Column(Float)
    nearest5_longitude = Column(Float)
    type_new = Column(String)
    near_mechanic = Column(Integer)

class Places(Base):
    __tablename__ = 'places'
    hash_index = Column(String, primary_key=True, index=True)
    name = Column(String)
    geometry = Column(String)
    amenity = Column(String)
    building = Column(String)
    shop = Column(String)
    highway = Column(String)
    latitude = Column(Float,index=True)
    longitude = Column(Float,index=True)

class Trips(Base):
    __tablename__ = 'trips'
    tractor_id = Column(String, primary_key=True, index=True)
    trip_id = Column(String, primary_key=True, index=True)
    country = Column(String)
    state = Column(String)
    city = Column(String)
    address = Column(String)
    postal_code = Column(String)
    latitude = Column(Float)
    longitude = Column(Float)
    odometer = Column(Float)
    load_status = Column(String)
    to_state = Column(String)
    to_city = Column(String)
    to_country = Column(String)
    to_address = Column(String)
    to_postal_code = Column(String)
    to_latitude = Column(Float)
    to_longitude = Column(Float)
    arrival_datetime = Column(String, primary_key=True, index=True)
    to_arrival_datetime = Column(String)
    trip_ref_norm = Column(String)
    gps = Column(JSON)
    fuel = Column(JSON)
    unit_tank = Column(Float)